        <h3>How do I sign a petition?</h3>
        <p>To sign a petition, navigate to the petition page and locate the "Sign Now" button. Click on it and provide the requested information, such as your name and email address. Optionally, you may have the option to add a comment or share the petition on social media.</p>
        
